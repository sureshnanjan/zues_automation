// Declaring and initializing a variable
let myAge = 24;

// Printing the value of the variable to the console
console.log("My age is:", myAge);

// Creating an array of numbers
const numbers = [1, 2, 3, 4, 5];

// Printing the array to the console
console.log("The numbers array is:", numbers);

// Accessing a specific element of the array
console.log("The second element of the numbers array is:", numbers[1]);

// Changing the value of an element in the array
numbers[2] = 10;
console.log("The numbers array after changing the third element is:", numbers);

// Creating an object
const person = {
  name: "BOB",
  age: 32,
  occupation: "Builder"
};

// Accessing a property of the object
console.log("The name of the person is:", person.name);

// Adding a new property to the object
person.hobbies = ["reading", "writing", "coding"];
console.log("The person object after adding hobbies is:", person);
